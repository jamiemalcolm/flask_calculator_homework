def add(num1, num2):
    num1 = int(num1)
    num2 = int(num2)
    return num1 + num2

def subtract(num1, num2):
    num1 = int(num1)
    num2 = int(num2)
    return num1 - num2

def divide(num1, num2):
    num1 = int(num1)
    num2 = int(num2)
    return num1 // num2

def multiply(num1, num2):
    num1 = int(num1)
    num2 = int(num2)
    return num1 * num2

def squared(num):
    num = int(num)
    return num**2